package labs_examples.datatypes_operators.labs;

/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        // write your code below
        int a = 4;
        int b = 9;

        int c = a + b;
        System.out.println("value for addition is 13: " + c);
        int d = b - a;
        System.out.println("value for subtraction is 5: " + d);
        int e = a * b;
        System.out.println("value for multiplication is 36: " + e);
        int f = b / a;
        System.out.println("value for division is 2: " + f);
        int g = b % a;
        System.out.println("value for modulus is 1: " + g);

    }

}
